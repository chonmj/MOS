function color = seven_colors(num)
    if num == 1
        color = 'g';
    elseif num == 2
        color = 'r';
    elseif num == 3
        color = 'b';
    elseif num == 4
        color = 'k';
    elseif num == 5
        color = 'c';
    elseif num == 6
        color = 'm';
    elseif num == 7
        color = 'y';
    else
        color = 'g';
    end

end